    <h1>Normativa Docente</h1>
<hr>

  <p>A normativa docente tem como objetivo acompanhar o desenvolvimento da Atividade Docente e fornecer subsídios à tomada de decisão, sendo observada a legislação vigente no que se refere à avaliação do estágio probatório; progressão funcional; participação em editais; participação em congressos, seminários e congêneres; liberação para capacitação e qualificação; concessão de Dedicação Exclusiva.</p>
  <p>No início de cada semestre, o docente deverá elaborar o seu Plano de Trabalho Docente sendo que este deverá ser comprovado por meio do Relatório de Atividades Docentes, ao final do mesmo semestre.</p>
  <p>Entende-se por Plano de Trabalho Docente o detalhamento das atividades a serem exercidas durante o semestre, com base nas pontuações e cargas horárias máximas específicas destinadas a cada uma das atividades.</p>
  <p>O Relatório de Trabalho Docente é o documento que certifica as atividades, derivado do Plano de Trabalho Docente, e o mesmo deverá ser validado por uma Comissão de Verificação.</p>
  <p>(Resolução nº 119/2016, de 15 de dezembro de 2016)</p>

