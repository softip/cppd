  <h1>Progressão / Promoção Funcional e Aceleração da Promoção</h1>
  <hr>
  <p>Progressão é a passagem do servidor para o padrão imediatamente superior dentro da classe ou categoria atual de sua Carreira Funcional. Já a Promoção é a passagem do servidor do último padrão de uma classe ou categoria para o primeiro padrão da classe ou categoria imediatamente superior de sua Carreira Funcional.</p> É por meio da progressão e da promoção que ocorre o desenvolvimento do servidor nas carreiras (ou cargos).</p>
  <p>Aceleração da promoção constitui-se da passagem do servidor:</p>
    <ul>
        <li>a) Após a aprovação no Estágio Probatório, da Classe A para a Classe B, se portador do diploma de Mestrado e da Classe A para a Classe C, se portador do diploma de Doutorado;</li>
        <li> b) Da Classe B para a Classe C, pela apresentação do diploma de Doutorado independente da data-base.</li>
    </ul>
  <p>Diretrizes gerais do processo de avaliação de desempenho docente são estabelecidas para fins de progressão funcional e promoção do servidor de Magistério de Ensino Básico, Técnico e Tecnológico do Plano de Carreiras e Cargos de Magistério Federal.</p>
  <p>A avaliação de desempenho docente compreende o acompanhamento sistemático e contínuo da atuação individual e institucional, considerando as metas do IFSULDEMINAS e os seguintes Grupos de exercício docente:
    <ul>
        <li>I. Docentes exclusivamente em atividades de ensino, pesquisa e extensão.</li>
        <li>II. Docentes em exercício exclusivo de cargos/função e/ou amparados pelo Art. 29, da Resolução CONSUP/IFSULDEMINAS no 108/2014, de 18 de dezembro de 2014.</li>
        <li>III. Docentes afastados com liberação integral para mestrado, doutorado ou pós-doutorado.</li>
        <li>IV. Docentes em atividades de ensino, pesquisa e extensão e em exercício de cargo ou função e coordenadores.</li>
        <li>V. Docentes em cooperação técnica.</li>
        <li>VI. Liberação de professores para programas de cooperação com outras instituições, universitárias ou não.</li>
    </ul>
    <p>(Resolução no 071/2015, de 17 de dezembro de 2015)</p>
