  <h1>Afastamento para capacitação</h1>
  <hr>
  <p>A concessão de afastamento para os servidores docentes é realizada com o objetivo de garantir e estimular o desenvolvimento individual e profissional dos servidores docentes do IFSULDEMINAS promovendo a qualificação do servidor e o progresso do sistema organizacional, visando a alimentar e disseminar a busca por conhecimento, bem como possibilitar a preparação do servidor para melhor desempenho das suas funções, com vistas à consolidação da pesquisa indissociada do ensino e da extensão.
</p>
  <p>Entende-se por qualificação o processo de aprendizagem baseado em ações de educação formal, por meio do qual o servidor adquire conhecimentos e habilidades, tendo em vista o planejamento institucional e o desenvolvimento do servidor na carreira. </p>
  <p>(Resolução nº 009/2017, de 27 de março de 2017)</p>
