    <h1>Reconhecimento de Saberes e Competências (RSC)</h1>
<hr>
  <p>O Reconhecimento de Saberes e Competências - RSC - é um direito previsto para a carreira do Magistério Federal de Ensino Básico, Técnico e Tecnológico, com base no art. 18 da lei 12.772/2012, que permite a percepção de Retribuição de Titulação equivalente à Especialização, Mestrado e Doutorado, sem o referido título.</p>
  <p>A equivalência do RSC com a titulação ocorre em 3 níveis:<br>
  <ul>
      <li>Diploma de graduação somado ao RSC-I equivale à titulação de especialização;</li>
	  <li>Diploma de pós-graduação lato sensu somado ao RSC-II equivale a mestrado;</li>
      <li>Diploma de Mestre somado ao RSC-III equivale a doutorado.</li>
    </ul>
  </p>
  <p>(Os procedimentos e normas gerais para a concessão do RSC são estabelecidas pela Resolução nº 1/2014, do Conselho Permanente para Reconhecimento de Saberes e Competências (CPRSC), instituído pelo MEC, a partir da qual cada IFE elaborou seu regulamento interno.</p>
  <p>(Resolução nº 72/2016, de 15 de dezembro de 2016.)</p> 
